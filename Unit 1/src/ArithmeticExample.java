public class ArithmeticExample {
    public static void main(String[] args){
        
        int num1 = 7;
        int num2 = 2; 
        
        System.out.println(num1 + "/" + num2 + " = " + num1/num2);
        
        double num3 = 3.0;
        
        System.out.println(num1 + "/" + num2 + " = " + num1/num3);
        
        System.out.println("The remainder of " + num1 + "/" + num2 + "is: " + num1%num2);
        
    } // end of main 
    
} // end of class

